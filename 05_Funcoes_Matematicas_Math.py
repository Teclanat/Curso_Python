#Importar a classe math
import math

#Calculos Exponenciais
x = 25
print(math.exp(x))

#Raiz Quadrada
print(math.sqrt(x))

#Logaritmo de Base 10
print(math.log10(x))

#Logaritmo natural de x
print(math.log(x))

#x elevado a y
print(math.pow(5,2))

#Seno de x
print(math.sin(1))

#Cosseno de x
print(math.cos(2))

#Tangente de x
print(math.tan(1))

#Constante Pi
print(math.pi)

#Constante e
print(math.e)

#Outra forma de exponenciais
print(2 ** 3)




